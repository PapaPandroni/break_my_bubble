// Helper function to merge multilanguage data with legacy topics
const enhanceTopicWithMultilanguage = (topic: TopicKeywords): TopicKeywords => {
  const multiLangTopic = MULTI_LANGUAGE_TOPICS.find(t => t.topic === topic.topic)
  if (multiLangTopic) {
    return {
      ...topic,
      multiLanguageKeywords: multiLangTopic.multiLanguageKeywords,
      fallbackKeywords: multiLangTopic.fallbackKeywords
    }
  }
  return topic
}

